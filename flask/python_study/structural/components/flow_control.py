# 흐름제어 (flow control)
# 순차, 선택, 반복


# 1. 순차 : 위에서 아래로 순서대로 - 가장 기본

# 2. 선택 : 실행할 명령어가 여러 개 있고 그 중에 하나를 내가 선택해서 실행 - 반드시 조건식 필요
# if : 조건이 참이면 실행, 거짓이면 넘어감
# if-else : 최소 1개 선택 (2개 중 무조건 하나만 선택할 때)
# if-elif : 여러 조건 중 첫 번째로 참인 것 하나 선택, 모두 거짓이면 넘어감
# if-elif-else : 여러 조건 중 첫 번째로 참인 것을 실행하고, 모두 거짓이면 else 실행

# 3. 반복 : 특정 명령어를 계속해서 반복적으로 실행 -> 참인 동안 계속 실행
# for : 반복 횟수 정해져 있을 때
# while : 반복 횟수 정해져 있지 않을 때